package com.github.tymefly.eeprom.builder.project.input;

/**
 * Enumeration of the names in the input pin configuration file. These describe the states of the system
 */
public enum InputTypes {
    /** Bits in the Instruction Register */
    IR,

    /** Bits in the Flags Register */
    FLAG,

    /** Bits in Machine Cycle Counter */
    M_CYCLE
}
